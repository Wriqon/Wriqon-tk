<div id="left">
<h2>Bases:</h2>

<h3>The toss</h3>
<ol>
	<li>Get into a good athletic stance
	<li>Grab your fliers hips (don't squeeze too hard- a relaxed grip is good)
	<li>Follow the flier and squat when she dips to start her jump (important to not push her down into the ground when she dips)
	<li>WAIT as she starts to explode upwards
	<li>Squeeze your elbows inwards to keep your arms in a power position
	<li>Use your legs and PUSH the ground away!
	<li>When you're nearly fully stood up start to use your arms to push your flier away
	<li>Your legs and arms should be fully extended at the end of this step
	<li>At the very top release your flier with a flick from your wrists. Don't stop your toss early in order to cheat and try to get to her feet quicker
</ol>

<h3>The catch</h3>
<ol>
	<li>Once you finish your toss and flick, bring your hands straight downwards to get underneath the feet
	<li>Open your hands as wide as you can to get the greatest amount of space to catch with
	<li>When your flier's feet make contact with your hands begin to squeeze them hard!
	<li>Tighten your abs and squeeze your shoulder blades to stabilize your arms
</ol>

<h3>The pop off and set down</h3>
<ol>
	<li>Start the pop by dipping slightly through your legs
	<li>Press through the ground and toss your flier up and <b>SLIGHTLY</b> forward
	<li>Reach up and catch your flier's hips in the same way as when you started
	<li>Slow your flier down as much as you can! A good pop off will use the same amount of effort as the toss to get up there
	<li>Once you both have your feet back on the ground, stand back up sharp!
</ol>

</div>

<div id="right">
<h2>Fliers:</h2>

<h3>The toss</h3>
<ol>
	<li>Start slightly on your toes with your elbows back and hands on your base's wrists
	<li>Lean back slightly and push down through your base's wrists
	<li>Start before the toss with a slight bounce up to get timing with your base
	<li>Dip low enough in your jump to get a good push off of the ground but not so much that your chest comes forward
	<li>Explode upwards as fast as you can while still keeping your chest up and pressure into your base's wrists
	<li>After you leave the ground your legs should be locked out until you pop off
	<li>Wait until the top and then flick your base's wrists straight down away from you to get the most height out of your toss
</ol>

<h3>The catch</h3>
<ol>
	<li>Keep your legs locked so that your base has something stable to hold on to (No spaghetti legs!!)
	<li>Suck your belly button in towards your spine and roll your shoulders up and back to help stay tight
	<li>When your base grabs your feet you should keep your center of balance in the back half of your foot near your heel
	<li>Pull a high V and smile!
</ol>

<h3>The pop off</h3>
<ol>
	<li>Keep your legs straight and locked out while your base dips and pops you off
	<li>Snap your arms to your side aggressively to perform a clean pop off
	<li>Lean back while you're in midair to help your base catch you and slow you down before you touch the ground
	<li>Once you feel your base's hands on your hips, grab on to his wrists again
	<li>Unlock your legs before you touch the ground to save from hurting your knees
	<li>After you stop on the ground, stand up sharp to finish
</ol>

